# 赋值运算符
var_1 = 1
var_2 = var_1
print(var_1, var_2, type(var_1), type(var_2), end="\n赋值运算符结束\n")

# 加法 addition
addition_var_1 = 1
addition_var_2 = 2
addition_result = addition_var_1 + addition_var_2
print(addition_result)
print(type(addition_result))

# 减法 subtraction
subtraction_var_1 = 1
subtraction_var_2 = 2
subtraction_result = subtraction_var_1 - subtraction_var_2
print(subtraction_result)
print(type(subtraction_result))

# 乘法 multiplication
multiplication_var_1 = 1
multiplication_var_2 = 2
multiplication_result = multiplication_var_1 * multiplication_var_2
print(multiplication_result)
print(type(multiplication_result))

# 除法 division / 输出为 float 类型
division_var_1 = 1
division_var_2 = 3
division_result = division_var_1 / division_var_2
print(division_result)
print(type(division_result))

# 整除 division // 输出为 int 类型
division_var_1 = 1
division_var_2 = 2
division_result = division_var_1 // division_var_2
print(division_result)
print(type(division_result))

# 模运算 mod % 输出为 int 类型
mod_var_1 = 1
mod_var_2 = 2
mod_result = mod_var_1 % mod_var_2
print(mod_result)
print(type(mod_result))

# 幂运算 pow ** 输出为 int 类型
pow_var_1 = 2
pow_var_2 = 2
pow_result = pow_var_1 ** pow_var_2
print(pow_result)
print(type(pow_result))
